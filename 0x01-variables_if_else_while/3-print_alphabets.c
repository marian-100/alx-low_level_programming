#include <stdio.h>
/**
  *main - Entry point
  *Description: 'A program that prints alphabets lowercase and uppercase'
  *Return: Always 0
  */
int main(void)
{
	char c;	
	char d;

	while 
		(c <= 'z') {
			putchar(c);
			c++;
		}
	while 
		(d <= 'z') {
			putchar(d);
			d++;
		}	
	putchar('\n');
	return (0);
}
